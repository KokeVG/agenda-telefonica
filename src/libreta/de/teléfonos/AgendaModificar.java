    private void box() {
        lista.setModel(getRespaldo());
        DefaultListModel nuevomodelo = new DefaultListModel();
        int i;
        if(box_trabajo.isSelected()) {
            for(i=0; i < lista.getModel().getSize(); i++) {
                if(lista.getModel().getElementAt(i).contains("TRABAJO")) {
                    nuevomodelo.addElement(lista.getModel().getElementAt(i));                        
                }
            }
        }
        if(box_hogar.isSelected()) {
            for(i=0; i < lista.getModel().getSize(); i++) {
                if(lista.getModel().getElementAt(i).contains("HOGAR")) {
                    nuevomodelo.addElement(lista.getModel().getElementAt(i));                        
                }
            }
        }
        if(box_movil.isSelected()) {
            for(i=0; i < lista.getModel().getSize(); i++) {
                if(lista.getModel().getElementAt(i).contains("MOVIL")) {
                    nuevomodelo.addElement(lista.getModel().getElementAt(i));                        
                }
            }
        }
        lista.setModel(nuevomodelo);
    }
    
