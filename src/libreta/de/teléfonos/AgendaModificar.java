    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        if(lista.getSelectedValue() == null ) 
        {
            JOptionPane.showMessageDialog(null, "Seleccione el contacto que desea modificar", "Error", JOptionPane.ERROR_MESSAGE);
        } 
        else {
        datos_contacto = lista.getSelectedValue();
        
        int i = 0;
        while(!"-".equals(datos_contacto.substring(i, i+1)))
        {
            i++;
        }
        nombre.setText(datos_contacto.substring(0, i-1));
        i=i+2;
        int j = i;
        while(!"-".equals(datos_contacto.substring(i, i+1)))
        {
            i++;
        }
        telefono.setText(datos_contacto.substring(j,i-1));
        
        String tip = datos_contacto.substring(i+2, datos_contacto.length());
        
        switch (tip) {
            case "TRABAJO":
                tipo.setSelectedIndex(0);
                break;
            case "HOGAR":
                tipo.setSelectedIndex(1);
                break;
            default:
                tipo.setSelectedIndex(2);
                break;
        }
        
        Contacto.setLocationRelativeTo(null);
        Contacto.setSize(270,220);
        Contacto.setVisible(true);
        }
    }//GEN-LAST:event_modificarActionPerformed

    private void actualizarlista(String contacto_modificado) {
        datos_modificados.clear();
        int i = 0;
        while(!lista.getModel().getElementAt(i).equals(datos_contacto))
        {
            datos_modificados.addElement(lista.getModel().getElementAt(i));
            i++;
        }
        datos_modificados.addElement(contacto_modificado);
        i++;
        
        while(i < lista.getModel().getSize())
        {
            datos_modificados.addElement(lista.getModel().getElementAt(i));
            i++;
        }
        lista.setModel(datos_modificados);
        setRespaldo(datos_modificados);
    }
    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        String nom = nombre.getText();
        String num = telefono.getText();
        String tipoo = tipo.getSelectedItem().toString();
        
        String datos_modificado = nom + " - " + num + " - " + tipoo;
        
        actualizarlista(datos_modificado);
        
        Contacto.dispose();
        JOptionPane.showMessageDialog(null, "Contacto modificado", "Info", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_aceptarActionPerformed

